# Concept list pe loop chalate waqt index bhi chahiye ho to .
#  Problem :fruits=["apple,"banana","mango"]
#Task:Har fruit ko uske index ke saath print karo:"0:apple"
fruits = ["apple", "banana", "mango"]
for i, fruit in enumerate(fruits):
    print(f"{i}: {fruit}")
# Output:
# 0: apple
# 1: banana  
# 2: mango
